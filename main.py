def ensure_xlsx():
    """Create the xlsx file with headers if it doesn't exist."""
    os.makedirs(os.path.dirname(XLSX_PATH), exist_ok=True)
    if not os.path.exists(XLSX_PATH):
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Approved Submissions"
        ws["A1"] = "UID"
        ws["B1"] = "Cookies"
        wb.save(XLSX_PATH)


def append_to_xlsx(uid: str, cookies: str):
    """Append a new approved submission row to the persistent xlsx file."""
    ensure_xlsx()
    wb = openpyxl.load_workbook(XLSX_PATH)
    ws = wb.active
    ws.append([uid, cookies])
    wb.save(XLSX_PATH)


def reset_xlsx():
    """Clear all data rows from xlsx, keeping only the header."""
    os.makedirs(os.path.dirname(XLSX_PATH), exist_ok=True)
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "Approved Submissions"
    ws["A1"] = "UID"
    ws["B1"] = "Cookies"
    wb.save(XLSX_PATH)


def get_xlsx_bytes() -> bytes:
    """Read the current xlsx file and return as bytes."""
    ensure_xlsx()
    with open(XLSX_PATH, "rb") as f:
        return f.read()


def get_xlsx_row_count() -> int:
    """Return number of data rows (excluding header) in xlsx."""
    ensure_xlsx()
    wb = openpyxl.load_workbook(XLSX_PATH, read_only=True)
    ws = wb.active
    count = ws.max_row - 1 if ws.max_row and ws.max_row > 1 else 0
    wb.close()
    return count


