async def send_review_card(chat_id: int, index: int, edit_message_id: int = None):
    """
    Send or edit a review card for the pending submission at `index`.
    edit_message_id: if provided, edit that message instead of sending a new one.
    """
    subs = get_pending_submissions()
    if not subs:
        await bot.send_message(
            chat_id, "✅ No pending submissions to review.", parse_mode=ParseMode.HTML
        )
        return

    # Clamp index
    index = max(0, min(index, len(subs) - 1))
    sub = subs[index]

    text = (
        f"📋 <b>Submission #{sub['id']}</b>  [{index + 1}/{len(subs)}]\n\n"
        f"👤 <b>User ID:</b> {sub['user_id']}\n"
        f"🔗 <b>Username:</b> @{sub['username'] or 'N/A'}\n"
        f"🆔 <b>Facebook UID:</b> <code>{sub['uid']}</code>\n"
        f"🍪 <b>Cookies:</b>\n<code>{sub['cookies'][:300]}{'...' if len(sub['cookies']) > 300 else ''}</code>\n\n"
        f"👤 <b>Generated Name:</b> {sub['gen_first_name']} {sub['gen_last_name']}\n"
        f"🔑 <b>Password:</b> <code>{sub['gen_password']}</code>\n"
        f"🕐 <b>Submitted:</b> {sub['submitted_at']}\n"
        f"📊 <b>Status:</b> {sub['status'].upper()}"
    )
    keyboard = review_inline_keyboard(sub["id"], index, len(subs))

    if edit_message_id:
        try:
            await bot.edit_message_text(
                text,
                chat_id=chat_id,
                message_id=edit_message_id,
                reply_markup=keyboard,
                parse_mode=ParseMode.HTML,
            )
        except Exception:
            await bot.send_message(
                chat_id, text, reply_markup=keyboard, parse_mode=ParseMode.HTML
            )
    else:
        await bot.send_message(
            chat_id, text, reply_markup=keyboard, parse_mode=ParseMode.HTML
        )


@router.callback_query(F.data.startswith("rev_"))
async def handle_review_callback(call: CallbackQuery):
    """Handle Previous, Next, Approve, Reject inline buttons in review."""
    if not is_admin(call.from_user.id):
        await call.answer("Unauthorized.", show_alert=True)
        return

    data = call.data  # e.g. "rev_next_2", "rev_approve_5"
    parts = data.split("_")
    action = parts[1]  # prev | next | approve | reject

    if action == "next":
        current_index = int(parts[2])
        await call.answer()
        await send_review_card(
            call.message.chat.id, current_index + 1, call.message.message_id
        )

    elif action == "prev":
        current_index = int(parts[2])
        await call.answer()
        await send_review_card(
            call.message.chat.id, current_index - 1, call.message.message_id
        )

    elif action == "approve":
        sub_id = int(parts[2])
        sub = get_submission_by_id(sub_id)
        if not sub or sub["status"] != "pending":
            await call.answer("Already processed.", show_alert=True)
            return

        update_submission_status(sub_id, "approved")
        add_balance(sub["user_id"], TASK_PRICE)

        # Save to persistent xlsx file
        append_to_xlsx(sub["uid"], sub["cookies"])

        # Check referral commission
        user = get_user(sub["user_id"])
        if user and user.get("referred_by"):
            commission = round(TASK_PRICE * REFERRAL_COMMISSION, 6)
            add_balance(user["referred_by"], commission)
            log_referral_earning(user["referred_by"], sub["user_id"], commission)

        # Notify the worker
        try:
            worker_lang = get_user_lang(sub["user_id"])
            await bot.send_message(
                sub["user_id"],
                TRANSLATIONS[worker_lang]["approved_notify"].format(amount=TASK_PRICE),
                parse_mode=ParseMode.HTML,
            )
        except Exception:
            pass

        await call.answer("✅ Approved!", show_alert=True)
        # Refresh — show next pending or updated list
        await send_review_card(call.message.chat.id, 0, call.message.message_id)

    elif action == "reject":
        sub_id = int(parts[2])
        sub = get_submission_by_id(sub_id)
        if not sub or sub["status"] != "pending":
            await call.answer("Already processed.", show_alert=True)
            return

        update_submission_status(sub_id, "rejected")

        # Notify the worker
        try:
            worker_lang = get_user_lang(sub["user_id"])
            await bot.send_message(
                sub["user_id"],
                TRANSLATIONS[worker_lang]["rejected_notify"],
                parse_mode=ParseMode.HTML,
            )
        except Exception:
            pass

        await call.answer("❌ Rejected.", show_alert=True)
        await send_review_card(call.message.chat.id, 0, call.message.message_id)


