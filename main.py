def ensure_user(user_id: int, username: str, full_name: str, referred_by: int = None):
    """Insert user if not exists. Handles referral linking."""
    conn = get_db()
    c = conn.cursor()
    c.execute("SELECT id FROM users WHERE id = ?", (user_id,))
    row = c.fetchone()
    if not row:
        c.execute(
            "INSERT INTO users (id, username, full_name, referred_by) VALUES (?, ?, ?, ?)",
            (user_id, username, full_name, referred_by),
        )
        conn.commit()
    else:
        # Update username/name in case they changed
        c.execute(
            "UPDATE users SET username = ?, full_name = ? WHERE id = ?",
            (username, full_name, user_id),
        )
        conn.commit()
    conn.close()


def get_user(user_id: int) -> dict | None:
    """Fetch a user row as a dict."""
    conn = get_db()
    c = conn.cursor()
    c.execute("SELECT * FROM users WHERE id = ?", (user_id,))
    row = c.fetchone()
    conn.close()
    return dict(row) if row else None


def get_user_lang(user_id: int) -> str:
    """Return the user's preferred language code."""
    user = get_user(user_id)
    if user:
        return user.get("language", "en")
    return "en"


def set_user_lang(user_id: int, lang: str):
    """Update user language preference."""
    conn = get_db()
    conn.execute("UPDATE users SET language = ? WHERE id = ?", (lang, user_id))
    conn.commit()
    conn.close()


def get_balance(user_id: int) -> float:
    """Return the user's current balance."""
    user = get_user(user_id)
    return user["balance"] if user else 0.0


def add_balance(user_id: int, amount: float):
    """Add an amount to a user's balance."""
    conn = get_db()
    conn.execute(
        "UPDATE users SET balance = balance + ? WHERE id = ?", (amount, user_id)
    )
    conn.commit()
    conn.close()


def deduct_balance(user_id: int, amount: float):
    """Deduct an amount from a user's balance."""
    conn = get_db()
    conn.execute(
        "UPDATE users SET balance = balance - ? WHERE id = ?", (amount, user_id)
    )
    conn.commit()
    conn.close()


def get_task_password() -> str:
    """Retrieve current task password from settings."""
    conn = get_db()
    c = conn.cursor()
    c.execute("SELECT value FROM settings WHERE key = 'task_password'")
    row = c.fetchone()
    conn.close()
    return row["value"] if row else DEFAULT_PASSWORD


def set_task_password(new_password: str):
    """Update the task password in settings."""
    conn = get_db()
    conn.execute(
        "UPDATE settings SET value = ? WHERE key = 'task_password'", (new_password,)
    )
    conn.commit()
