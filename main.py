def get_db() -> sqlite3.Connection:
    """Return a SQLite connection with row_factory for dict-like access."""
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db():
    """Initialize all database tables."""
    conn = get_db()
    c = conn.cursor()

    # Users table
    c.execute("""
        CREATE TABLE IF NOT EXISTS users (
            id          INTEGER PRIMARY KEY,
            username    TEXT,
            full_name   TEXT,
            balance     REAL DEFAULT 0.0,
            language    TEXT DEFAULT 'en',
            referred_by INTEGER,
            joined_at   TEXT DEFAULT (datetime('now'))
        )
    """)

    # Submissions table
    c.execute("""
        CREATE TABLE IF NOT EXISTS submissions (
            id              INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id         INTEGER,
            username        TEXT,
            uid             TEXT,
            cookies         TEXT,
            gen_first_name  TEXT,
            gen_last_name   TEXT,
            gen_password    TEXT,
            status          TEXT DEFAULT 'pending',
            submitted_at    TEXT DEFAULT (datetime('now')),
            reviewed_at     TEXT
        )
    """)

    # Withdrawals table
    c.execute("""
        CREATE TABLE IF NOT EXISTS withdrawals (
            id          INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id     INTEGER,
            wallet      TEXT,
            amount      REAL,
            status      TEXT DEFAULT 'pending',
            requested_at TEXT DEFAULT (datetime('now'))
        )
    """)

    # Settings table (bot-wide config like current password)
    c.execute("""
        CREATE TABLE IF NOT EXISTS settings (
            key   TEXT PRIMARY KEY,
            value TEXT
        )
    """)
    # Insert default password if not present
    c.execute(
        "INSERT OR IGNORE INTO settings (key, value) VALUES ('task_password', ?)",
        (DEFAULT_PASSWORD,),
    )

    # Referral earnings log
    c.execute("""
        CREATE TABLE IF NOT EXISTS referrals (
            id          INTEGER PRIMARY KEY AUTOINCREMENT,
            referrer_id INTEGER,
            referred_id INTEGER,
            amount      REAL,
            earned_at   TEXT DEFAULT (datetime('now'))
        )
    """)

    conn.commit()
    conn.close()


