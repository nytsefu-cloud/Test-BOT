def get_user_submission_stats(user_id: int) -> dict:
    """Return submission counts grouped by status for a user."""
    conn = get_db()
    c = conn.cursor()
    stats = {"submitted": 0, "approved": 0, "rejected": 0, "pending": 0}
    c.execute(
        "SELECT status, COUNT(*) as cnt FROM submissions WHERE user_id = ? GROUP BY status",
        (user_id,),
    )
    for row in c.fetchall():
        s = row["status"]
        if s in stats:
            stats[s] = row["cnt"]
    # Total submitted = sum of all statuses
    stats["submitted"] = stats["approved"] + stats["rejected"] + stats["pending"]
    conn.close()
    return stats


