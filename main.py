def get_referral_stats(user_id: int, bot_username: str) -> dict:
    """Return referral stats for a user."""
    conn = get_db()
    c = conn.cursor()
    now = datetime.utcnow()

    # Total referrals
    c.execute(
        "SELECT COUNT(*) as cnt FROM users WHERE referred_by = ?", (user_id,)
    )
    total = c.fetchone()["cnt"]

    # Joined last 24h
    since_24h = (now - timedelta(hours=24)).strftime("%Y-%m-%d %H:%M:%S")
    c.execute(
        "SELECT COUNT(*) as cnt FROM users WHERE referred_by = ? AND joined_at >= ?",
        (user_id, since_24h),
    )
    last24 = c.fetchone()["cnt"]

    # Earned last 30 days
    since_30d = (now - timedelta(days=30)).strftime("%Y-%m-%d %H:%M:%S")
    c.execute(
        "SELECT COALESCE(SUM(amount), 0) as total FROM referrals WHERE referrer_id = ? AND earned_at >= ?",
        (user_id, since_30d),
    )
    earned30 = c.fetchone()["total"]

    # Earned last 24h
    c.execute(
        "SELECT COALESCE(SUM(amount), 0) as total FROM referrals WHERE referrer_id = ? AND earned_at >= ?",
        (user_id, since_24h),
    )
    earned24 = c.fetchone()["total"]

    conn.close()
    return {
        "total": total,
        "last24": last24,
        "earned30": earned30,
        "earned24": earned24,
        "bot_username": bot_username,
        "user_id": user_id,
    }


